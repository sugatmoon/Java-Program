public class relationalOperater {
    public static void main(String[] args) {
        boolean num1 = true;
        boolean num2 = false;
//        For logical Add operater
        if (num1 && num2) {
            System.out.println("This is Add logical Operater");
        }
        else {
            System.out.println("OK, its works finaly");
        }
//       For logical OR operater
        if (num1 || num2) {
            System.out.println("This is Add logical Operater");
        }
        else {
            System.out.println("OK, its works finaly");
        }
//       For logical NOT operater
        System.out.println(!num1);
    }
}
