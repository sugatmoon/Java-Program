public class For_loop {
    public static void main(String[] args) {
//        Incremental For loop(To print froward order multiple of 2 to 10
        for(int i=1; i <=10; i++){
            System.out.println(2*i);
        }
//        Decremental For loop(To print froward order multiple of 2 to 10
        for(int i = 10; i>=1;i--){
            System.out.println(2*i);
        }
    }
}
